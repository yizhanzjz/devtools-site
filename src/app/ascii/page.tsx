const fontNames: Record<FontStyle, string> = {
  standard: "标准字体",
  banner: "横幅字体",
  block: "方块字体",
  shadow: "阴影字体",
  slant: "倾斜字体",
  bubble: "气泡字体",
};

const exampleTexts = ["HELLO", "ASCII", "DEVTOOLS", "2026", "CODE"];

export default function AsciiPage() {
  const [input, setInput] = useState("HELLO");
  const [selectedFont, setSelectedFont] = useState<FontStyle>("standard");
  const [output, setOutput] = useState<string>("");

  const generateAscii = useCallback((text: string, font: FontStyle) => {
    if (!text) {
      setOutput("");
      return;
    }

    const upperText = text.toUpperCase();
    const fontData = fonts[font];
    const lines: string[] = ["", "", "", "", ""];

    for (const char of upperText) {
      const charData = fontData[char] || fontData[" "];
      if (charData) {
        for (let i = 0; i < 5; i++) {
          lines[i] += charData[i];
        }
      }
    }

    setOutput(lines.join("\n"));
  }, []);

  useEffect(() => {
    generateAscii(input, selectedFont);
  }, [input, selectedFont, generateAscii]);

  return (
    <ToolLayout
      title="ASCII 艺术"
      description="将文本转换为 ASCII 艺术字，支持多种字体风格"
    >
      <div>
        <label className="block text-sm text-dark-300 mb-2">输入文字</label>
        <input
          type="text"
          className="w-full px-4 py-2 bg-dark-800 border border-dark-600 rounded-lg text-dark-100 placeholder-dark-400 focus:outline-none focus:border-accent"
          value={input}
          onChange={(e) => setInput(e.target.value)}
          placeholder="输入英文字母或数字..."
          spellCheck={false}
        />
      </div>

      <div>
        <label className="block text-sm text-dark-300 mb-2">字体风格</label>
        <div className="grid grid-cols-2 sm:grid-cols-3 gap-2">
          {(Object.keys(fonts) as FontStyle[]).map((font) => (
            <button
              key={font}
              onClick={() => setSelectedFont(font)}
              className={`px-4 py-2 rounded-lg border transition-colors ${
                selectedFont === font
                  ? "bg-accent border-accent text-dark-950"
                  : "bg-dark-800 border-dark-600 text-dark-200 hover:border-dark-500"
              }`}
            >
              {fontNames[font]}
            </button>
          ))}
        </div>
      </div>

      <div>
        <label className="block text-sm text-dark-300 mb-2">预设示例</label>
        <div className="flex flex-wrap gap-2">
          {exampleTexts.map((text) => (
            <button
              key={text}
              onClick={() => setInput(text)}
              className="px-3 py-1 bg-dark-800 border border-dark-600 rounded text-sm text-dark-200 hover:border-dark-500 transition-colors"
            >
              {text}
            </button>
          ))}
        </div>
      </div>

      {output && (
        <div className="space-y-3">
          <div className="flex items-center justify-between">
            <label className="text-sm text-dark-300">生成结果</label>
            <CopyButton text={output} />
          </div>
          <div className="p-4 bg-dark-900 border border-dark-700 rounded-lg overflow-x-auto">
            <pre className="text-accent font-mono text-sm leading-tight whitespace-pre">
              {output}
            </pre>
          </div>
        </div>
      )}
    </ToolLayout>
  );
}

